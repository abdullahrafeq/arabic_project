def convert_to_arabic_numerals(year):
    arabic_numerals = {
        '0': '٠',
        '1': '١',
        '2': '٢',
        '3': '٣',
        '4': '٤',
        '5': '٥',
        '6': '٦',
        '7': '٧',
        '8': '٨',
        '9': '٩'
    }
    return ''.join(arabic_numerals[digit] for digit in str(year))
